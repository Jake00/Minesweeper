import UIKit

extension UIColor {
    
    class func midGrayColor() -> UIColor {
        return UIColor(white: 0.69, alpha: 1)
    }
    
    class func bevelGrayColor() -> UIColor {
        return UIColor(white: 0.41, alpha: 1)
    }
}
